/**
 * Create a Ball with a given height, width and length
 */
Ball::Ball(glm::vec3 params, glm::vec3 position) {
	// Create and scale the model matrix
	m_position = position;
	m_scale = params / 2.0f;
	m_model = glm::mat4(1.0f);
	m_model = glm::translate(m_model, m_position);
	m_model = glm::scale(m_model, m_scale);

	// We only create the ball GL objects (VAO, VBO, IBO) if we
	// haven't created them yet
	if (s_num_balls++ == 0) CreateGLObjects();
}

